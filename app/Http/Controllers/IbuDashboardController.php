<?php

namespace App\Http\Controllers;

class IbuDashboardController extends Controller
{
    public function index()
    {
        /*
        |--------------------------------------------------------------------------
        | User Login
        |--------------------------------------------------------------------------
        */

        $user = auth()->user();

        /*
        |--------------------------------------------------------------------------
        | Data Ibu
        |--------------------------------------------------------------------------
        */

        $mother = $user->mother;

        /*
        |--------------------------------------------------------------------------
        | Data Anak
        |--------------------------------------------------------------------------
        */

        $children = $mother?->children ?? [];

        return view('ibu.dashboard', compact(

            'mother',
            'children'

        ));
    }
}